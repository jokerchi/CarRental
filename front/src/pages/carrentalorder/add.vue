<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
          orderNumber : false,
          carNumber : false,
          carBrand : false,
          dailyPrice : false,
          leaseTerm : false,
          totalPrice : false,
          startDate : false,
          remark : false,
          clientAccount : false,
          clientName : false,
          phone : false,
          idCard : false,
          administratorAccount : false,
          administratorName : false,
          isReviewed : false,
          reviewReply : false,
          isPay : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          orderNumber: this.getUUID(),
          carNumber: '',
          carBrand: '',
          dailyPrice: '',
          leaseTerm: '',
          totalPrice: '',
          startDate: '',
          remark: '',
          clientAccount: '',
          clientName: '',
          phone: '',
          idCard: '',
          administratorAccount: '',
          administratorName: '',
          isPay: '',
        },
        rules: {
          orderNumber: [
          ],
          carNumber: [
          ],
          carBrand: [
          ],
          dailyPrice: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          leaseTerm: [
            { required: true, message: '租期不能为空', trigger: 'blur' },
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          totalPrice: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          startDate: [
          ],
          remark: [
          ],
          clientAccount: [
          ],
          clientName: [
          ],
          phone: [
            { validator: this.$validate.isMobile, trigger: 'blur' },
          ],
          idCard: [
          ],
          administratorAccount: [
          ],
          administratorName: [
          ],
          isReviewed: [
          ],
          reviewReply: [
          ],
          isPay: [
          ],
        },
      };
    },
    computed: {


      totalPrice:{
            get: function () {
                return 1*this.ruleForm.dailyPrice*this.ruleForm.leaseTerm
            }
        },

    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.startDate = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='orderNumber'){
              this.ruleForm.orderNumber = obj[o];
              this.ro.orderNumber = true;
              continue;
            }
            if(o=='carNumber'){
              this.ruleForm.carNumber = obj[o];
              this.ro.carNumber = true;
              continue;
            }
            if(o=='carBrand'){
              this.ruleForm.carBrand = obj[o];
              this.ro.carBrand = true;
              continue;
            }
            if(o=='price'){
              this.ruleForm.dailyPrice = obj[o];
              this.ro.dailyPrice = true;
              continue;
            }
            if(o=='leaseTerm'){
              this.ruleForm.leaseTerm = obj[o];
              this.ro.leaseTerm = true;
              continue;
            }
            if(o=='totalPrice'){
              this.ruleForm.totalPrice = obj[o];
              this.ro.totalPrice = true;
              continue;
            }
            if(o=='startDate'){
              this.ruleForm.startDate = obj[o];
              this.ro.startDate = true;
              continue;
            }
            if(o=='remark'){
              this.ruleForm.remark = obj[o];
              this.ro.remark = true;
              continue;
            }
            if(o=='clientAccount'){
              this.ruleForm.clientAccount = obj[o];
              this.ro.clientAccount = true;
              continue;
            }
            if(o=='clientName'){
              this.ruleForm.clientName = obj[o];
              this.ro.clientName = true;
              continue;
            }
            if(o=='phone'){
              this.ruleForm.phone = obj[o];
              this.ro.phone = true;
              continue;
            }
            if(o=='idCard'){
              this.ruleForm.idCard = obj[o];
              this.ro.idCard = true;
              continue;
            }
            if(o=='ordinaryAdminAccount'){ //来自carinfo,所以要用carinfoh的属性
             this.ruleForm.administratorAccount = obj[o];
              this.ro.administratorAccount = true;
              continue;
            }
            if(o=='ordinaryAdminName'){ //来自carinfo,所以要用carinfoh的属性
              this.ruleForm.administratorName = obj[o];
              this.ro.administratorName = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.account!=''&&json.account) || json.account==0){
                this.ruleForm.clientAccount = json.account
            }
            if((json.name!=''&&json.name) || json.name==0){
                this.ruleForm.clientName = json.name
            }
            if((json.phone!=''&&json.phone) || json.phone==0){
                this.ruleForm.phone = json.phone
            }
            if((json.idCard!=''&&json.idCard) || json.idCard==0){
                this.ruleForm.idCard = json.idCard
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('carrentalorder/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

          this.ruleForm.totalPrice = this.totalPrice
        //更新跨表属性
        var crossUserId;
        var crossRefId;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                       crossUserId=Number(localStorage.getItem('userid'));
                       crossRefId=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossRefId && crossUserId) {
                 this.ruleForm.crossUserId=crossUserId;
                 this.ruleForm.crossRefId=crossRefId;
                 var params = {
                     page: 1,
                     limit: 10,
                   crossUserId:crossUserId,
                   crossRefId:crossRefId,
                 }
                 this.$http.get('carrentalorder/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('carrentalorder/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('carrentalorder/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

