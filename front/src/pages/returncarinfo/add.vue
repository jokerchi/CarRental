              this.ruleForm.adminName = obj[o];
              this.ro.adminName = true;
              continue;
            }
            if(o=='crossUserId'){
              this.ruleForm.crossUserId = obj[o];
              this.ro.crossUserId = true;
              continue;
            }
            if(o=='crossRefId'){
              this.ruleForm.crossRefId = obj[o];
              this.ro.crossRefId = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.clientAccount!=''&&json.clientAccount) || json.clientAccount==0){
                this.ruleForm.clientAccount = json.clientAccount
            }
            if((json.clientName!=''&&json.clientName) || json.clientName==0){
                this.ruleForm.clientName = json.clientName
            }
            if((json.phone!=''&&json.phone) || json.phone==0){
                this.ruleForm.phone = json.phone
            }
            if((json.idCard!=''&&json.idCard) || json.idCard==0){
                this.ruleForm.idCard = json.idCard
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('returncarinfo/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossUserId;
        var crossRefId;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                       crossUserId=Number(localStorage.getItem('userid'));
                       crossRefId=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossRefId && crossUserId) {
                 this.ruleForm.crossUserId=crossUserId;
                 this.ruleForm.crossRefId=crossRefId;
                 var params = {
                     page: 1,
                     limit: 10,
                   crossUserId:crossUserId,
                   crossRefId:crossRefId,
                 }
                 this.$http.get('returncarinfo/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('returncarinfo/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('returncarinfo/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

